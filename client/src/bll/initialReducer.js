

const initialState = {
    initialData: false
}


export const initialReducer = (state = initialState, action)  => {
    switch (action.type) {
        case "QUIZ/INITIAL":

            return {...state, initialData: action.data
    }

        default:
            return state
    }
}



export const setInitialDataAC = (data) =>
    ({type: 'QUIZ/INITIAL', data})